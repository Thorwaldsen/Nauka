# Najbardziej podstawowa klasa na świecie.
class Klasa:
    pass


# Utworzenie instancji klasy.
instancja_klasy = Klasa()
print(instancja_klasy, type(instancja_klasy))


# Nadal prosty, ale już bardziej użyteczny przykład klasy.
class Klasa2:
    x = 10  # Atrybut.


instancja_klasy2 = Klasa2()
print(instancja_klasy2.x)  # Pobranie wartości atrybutu 'x' z 'Klasa2'.
